function batmanVillains(villain) {
	if (villain == "Joker") {
		console.log("Why so serious?");
	}
	else if (villain == "Riddler") {
		console.log("It Can Be Cruel, Poetic Or Blind. But When It's Denied, It's Violence You May Find.");
	}
	else if (villain == "Catwoman") {
		console.log("I am Catwoman. Hear me roar.");
	}
	else if (villain == "Penguin") {
		console.log("My Name Is Not Oswald! It's Penguin. I Am Not A Human Being! I Am An Animal!");
	}
	else if (villain == "Ivy") {
		console.log("They can bury us deep, but we always grow back.");
	}
	else {
		alert("Who is this person?");
	}
}

batmanVillains("Joker");
batmanVillains("Riddler");
batmanVillains("Catwoman");
batmanVillains("Penguin");
batmanVillains("Ivy");
batmanVillains("Person");